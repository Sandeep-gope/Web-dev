// Use of inbuilt objects in JS
// Functions like:
// PI,max,min,loor,ceil,abs,random,log,pow,tan
// sin,cos,sqrt,etc

// Use of "Math" object

console.log(Math.PI);

console.log(Math.max(27,45,87,35,98));

console.log(Math.min(27,45,87,35,98));

console.log(Math.round(24.49));

// It will print just the smallest integer from the value
console.log(Math.floor(1.1));

// It will print just the greatest integer from the value
console.log(Math.ceil(1.1));

// Converts every value to the positive value
console.log(Math.abs(-5));

// It provides any random number between 0 and 1
console.log(Math.random());

console.log(Math.sqrt(25));

console.log(Math.pow(2,10));

console.log(Math.cbrt(27));